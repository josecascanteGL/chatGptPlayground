namespace UnicornShopLegacy
{
    public class WebApiApplication : System.Web.HttpApplication
    {
        protected void Application_Start()
        {
            Populate_DB_If_None(); // Check and populate the database with unicorn data if it doesn't exist
            GlobalConfiguration.Configure(WebApiConfig.Register); // Configure Web API routes
        }

        private static void Populate_DB_If_None()
        {
            var context = new UnishopEntities(); // Create an instance of the database context

            if (!context.Database.Exists()) // Check if the database already exists
            {
                context.Database.Create(); // If database doesn't exist, create it

                // Directly connecting to the database to add a computed column to the app_unicorn table using SQL statement
                // This approach is used because computed column can't be generated by EF6 given a database-first approach.
                SqlConnection sqlCon = new SqlConnection(@"Data Source=db.unishop.local,1433\unishop; initial catalog=Unishop; User ID=admin; Password=dMdLgX6sZoXmOU2rnWTS;");
                SqlDataAdapter sqlda = new SqlDataAdapter("ALTER TABLE inventory ADD year_model AS (datepart(year,date_create)); ", sqlCon);
                sqlda.Fill(new DataTable());

                string[] readText = File.ReadAllLines(HttpContext.Current.Server.MapPath(".") + @"\unicorns.csv"); // Read unicorn data from a CSV file
                IList<inventory> newunicorns = new List<inventory>(); // Create a list to store new unicorn entries

                foreach (var line in readText) // Process each line of data from the CSV file
                {
                    string[] fields = line.Split(','); // Split the line by comma to get individual fields
                    var unicorn = new inventory(); // Create a new inventory object (unicorn)
                    unicorn.unicorn_id = Guid.Parse(fields[0]); // Parse and assign unicorn ID
                    unicorn.name = fields[1]; // Assign unicorn name
                    unicorn.description = fields[2]; // Assign unicorn description
                    unicorn.price = Convert.ToDecimal(fields[3]); // Parse and assign unicorn price
                    unicorn.image = fields[4]; // Assign unicorn image path
                    unicorn.date_create = Convert.ToDateTime(fields[5]); // Parse and assign creation date

                    newunicorns.Add(unicorn); // Add the new unicorn entry to the list
                }

                context.inventories.AddRange(newunicorns); // Add all new unicorn entries to the database context
                context.SaveChanges(); // Save changes to the database
            }
        }
    }
}